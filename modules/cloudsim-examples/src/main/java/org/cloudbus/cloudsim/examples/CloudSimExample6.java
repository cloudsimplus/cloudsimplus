/**
 * An example showing how to create scalable simulations.
 */
public class CloudSimExample6 {

    /** The cloudlet list. */
    private static List<Cloudlet> cloudletList;

    /** The vmlist. */
    private static List<Vm> vmlist;

    private static List<Vm> createVM(int userId, int vms) {
            //Creates a container to store VMs. This list is passed to the broker later
            List<Vm> list = new LinkedList<>();

            //VM Parameters
            long size = 10000; //image size (MB)
            int ram = 512; //vm memory (MB)
            int mips = 1000;
            long bw = 1000;
            int pesNumber = 1; //number of cpus
            String vmm = "Xen"; //VMM name

            //create VMs
            Vm[] vm = new Vm[vms];

            for(int i=0;i<vms;i++){
                    vm[i] = new VmSimple(i, userId, mips, pesNumber, ram, bw, size, vmm, new CloudletSchedulerTimeShared());
                    //for creating a VM with a space shared scheduling policy for cloudlets:
                    //vm[i] = VmSimple(i, userId, mips, pesNumber, ram, bw, size, priority, vmm, new CloudletSchedulerSpaceShared());

                    list.add(vm[i]);
            }

            return list;
    }


    private static List<Cloudlet> createCloudlet(int userId, int cloudlets){
            // Creates a container to store Cloudlets
            List<Cloudlet> list = new LinkedList<>();

            //cloudlet parameters
            long length = 1000;
            long fileSize = 300;
            long outputSize = 300;
            int pesNumber = 1;
            UtilizationModel utilizationModel = new UtilizationModelFull();

            Cloudlet[] cloudlet = new Cloudlet[cloudlets];

            for(int i=0;i<cloudlets;i++){
                    cloudlet[i] = new CloudletSimple(i, length, pesNumber, fileSize, outputSize, utilizationModel, utilizationModel, utilizationModel);
                    // setting the owner of these Cloudlets
                    cloudlet[i].setUserId(userId);
                    list.add(cloudlet[i]);
            }

            return list;
    }

    /**
     * Creates main() to run this example
     * @param args
     */
    public static void main(String[] args) {
            Log.printFormattedLine("Starting %s...", CloudSimExample6.class.getSimpleName());
            try {
                    // First step: Initialize the CloudSim package. It should be called
                    // before creating any entities.
                    int num_user = 1;   // number of grid users
                    Calendar calendar = Calendar.getInstance();
                    boolean trace_flag = false;  // mean trace events

                    // Initialize the CloudSim library
                    CloudSim.init(num_user, calendar, trace_flag);

                    // Second step: Create Datacenters
                    //Datacenters are the resource providers in CloudSim. We need at list one of them to run a CloudSim simulation
                    @SuppressWarnings("unused")
                    Datacenter datacenter0 = createDatacenter("Datacenter_0");
                    @SuppressWarnings("unused")
                    Datacenter datacenter1 = createDatacenter("Datacenter_1");

                    //Third step: Create Broker
                    DatacenterBroker broker = createBroker();
                    int brokerId = broker.getId();

                    //Fourth step: Create VMs and Cloudlets and send them to broker
                    vmlist = createVM(brokerId,20); //creating 20 vms
                    cloudletList = createCloudlet(brokerId,40); // creating 40 cloudlets

                    broker.submitVmList(vmlist);
                    broker.submitCloudletList(cloudletList);

                    // Fifth step: Starts the simulation
                    CloudSim.startSimulation();

                    // Final step: Print results when simulation is over
                    List<Cloudlet> newList = broker.getCloudletReceivedList();

                    CloudSim.stopSimulation();

                    TableBuilderHelper.print(new TextTableBuilder(), newList);
                    Log.printFormattedLine("%s finished!", CloudSimExample6.class.getSimpleName());

            }
            catch (Exception e)
            {
                    e.printStackTrace();
                    Log.printLine("The simulation has been terminated due to an unexpected error");
            }
    }

    private static Datacenter createDatacenter(String name){

            // Here are the steps needed to create a DatacenterSimple:
            // 1. We need to create a list to store one or more
            //    Machines
            List<Host> hostList = new ArrayList<>();

            // 2. A Machine contains one or more PEs or CPUs/Cores. Therefore, should
            //    create a list to store these PEs before creating
            //    a Machine.
            List<Pe> peList1 = new ArrayList<>();

            int mips = 1000;

            // 3. Create PEs and add these into the list.
            //for a quad-core machine, a list of 4 PEs is required:
            peList1.add(new PeSimple(0, new PeProvisionerSimple(mips))); // need to store Pe id and MIPS Rating
            peList1.add(new PeSimple(1, new PeProvisionerSimple(mips)));
            peList1.add(new PeSimple(2, new PeProvisionerSimple(mips)));
            peList1.add(new PeSimple(3, new PeProvisionerSimple(mips)));

            //Another list, for a dual-core machine
            List<Pe> peList2 = new ArrayList<>();

            peList2.add(new PeSimple(0, new PeProvisionerSimple(mips)));
            peList2.add(new PeSimple(1, new PeProvisionerSimple(mips)));

            //4. Create Hosts with its id and list of PEs and add them to the list of machines
            int hostId=-1;
            int ram = 2048; //host memory (MB)
            long storage = 1000000; //host storage
            long bw = 10000;

            hostList.add(new HostSimple(
                            ++hostId,
                            new ResourceProvisionerSimple(new Ram(ram)),
                            new ResourceProvisionerSimple(new Bandwidth(bw)),
                            storage,
                            peList1,
                            new VmSchedulerTimeShared(peList1)
                    )
            ); // This is our first machine

            hostList.add(new HostSimple(
                            ++hostId,
                            new ResourceProvisionerSimple(new Ram(ram)),
                            new ResourceProvisionerSimple(new Bandwidth(bw)),
                            storage,
                            peList2,
                            new VmSchedulerTimeShared(peList2)
                    )
            ); // Second machine


            // 5. Create a DatacenterCharacteristics object that stores the
            //    properties of a data center: architecture, OS, list of
            //    Machines, allocation policy: time- or space-shared, time zone
            //    and its price (G$/Pe time unit).
            String arch = "x86";      // system architecture
            String os = "Linux";          // operating system
            String vmm = "Xen";
            double time_zone = 10.0;         // time zone this resource located
            double cost = 3.0;              // the cost of using processing in this resource
            double costPerMem = 0.05;		// the cost of using memory in this resource
            double costPerStorage = 0.1;	// the cost of using storage in this resource
            double costPerBw = 0.1;			// the cost of using bw in this resource
            List<FileStorage> storageList = new LinkedList<>();	//we are not adding SAN devices by now

            DatacenterCharacteristics characteristics = new DatacenterCharacteristicsSimple (
            arch, os, vmm, hostList, time_zone, cost, costPerMem, costPerStorage, costPerBw);


            // 6. Finally, we need to create a DatacenterSimple object.
            Datacenter datacenter = null;
            try {
                    datacenter = new DatacenterSimple(name, characteristics, new VmAllocationPolicySimple(hostList), storageList, 0);
            } catch (Exception e) {
                    e.printStackTrace();
            }

            return datacenter;
    }

    //We strongly encourage users to develop their own broker policies, to submit vms and cloudlets according
    //to the specific rules of the simulated scenario
    private static DatacenterBroker createBroker(){

            DatacenterBroker broker = null;
            try {
                    broker = new DatacenterBrokerSimple("Broker");
            } catch (Exception e) {
                    e.printStackTrace();
                    return null;
            }
            return broker;
    }
}
